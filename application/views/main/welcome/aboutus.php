    <div class="zhu_b"><p>瑶琳国家森林公园是瑶琳森林公园始建于1993年，2004年通过招商引资，成立桐庐奇纬旅游投资开发有限公司，2008年12月被国家林业局批准定名为“浙江桐庐瑶琳国家森林公园”，是继桐庐县大奇山国家森林公园后的又一个国家级森林公园，是集溶洞、石林、密林、古村宅等自然景观和人文景观融合一体的生态型森林公园。</p><p>一期投资3亿元，建设范围10000㎡，开发了以山、水、洞为特色的景区，由于区位为中亚热带北缘，冬暖夏凉、四季皆宜，是旅游消
            夏避暑最佳胜地，素有“小庐山”的美称；二期预计总投入5亿元，规划范围26000㎡，以休闲度假、科考探险及商务会展为主题。</p></div>
